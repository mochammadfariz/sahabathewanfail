                    <p>B. Hewan Rabies</p>
                    <p>Ciri-ciri kucing yang yang terjangkit rabies sebagai berikut : </p>
                    <center><img height="300px" width ="auto" src="asset/img/desainpelaporan/kucingrabies.jpg" alt=""></center>
                    <h3 class="mt-3">Perhatikan gejala awal rabies.</h3>
                    <p class="mt-3">Perhatikan gejala awal rabies. Tahap awal rabies dapat berlangsung selama dua sampai sepuluh hari. Dalam jangka waktu tersebut, umumnya kucing akan terlihat sakit dengan gejala yang tidak spesifik. Gejala-gejala awal yang tidak spesifik tersebut adalah:</p>
                    <li>nyeri otot</li>
                    <li>gelisah</li>
                    <li>mudah marah</li>
                    <li>suhu tubuh yang fluktuatif</li>
                    <li>demam</li>
                    <li>ketidaknyamanan yang sulit dijelaskan</li>
                    <li>takut difoto, sebagai representasi rasa takut terhadap cahaya</li>
                    <li>anoreksia atau hilangnya ketertarikan terhadap makanan</li>
                    <li>muntah</li>
                    <li>diare</li>
                    <li>batuk</li>
                    <li>kesulitan menelan</li>
                    <center><img class="mt-3" height="300px" width ="auto" src="asset/img/desainpelaporan/kucingrabies2.jpg" alt=""></center>
                    <h3 class="mt-3">1. Periksa tubuh kucing untuk menemukan bekas gigitan.</h3>
                    <p class="mt-3">Jika curiga kucing Anda telah melakukan kontak fisik dengan hewan yang terinfeksi rabies, cobalah memeriksa tubuhnya untuk menemukan bekas gigitan. Faktanya, virus rabies dapat bertahan hidup di kulit atau bulu kucing hingga dua jam. Oleh karena itu, pastikan Anda selalu mengenakan sarung tangan, baju lengan panjang, dan celana panjang saat memeriksa tubuhnya. Virus rabies berpindah melalui air liur. Itulah mengapa penyakit rabies ditularkan melalui gigitan. Setelah masuk ke dalam tubuh, virus rabies akan segera menyebar melalui saraf menuju area tulang belakang dan otak kucing.[3] Segera bawa kucing Anda ke dokter hewan jika menemukan tanda-tanda berikut:
                    </p>
                    <li>bekas gigitan</li>
                    <li>keropeng</li>
                    <li>bekas cakaran</li>
                    <li>bulu yang berantakan dengan bekas air liur yang mengering</li>
                    <li>luka bernanah</li>
                    <h3 class="mt-3">2. Waspadai tanda-tanda “kelinglungan” atau kelumpuhan.</h3>
                    <p class="mt-3">Rabies kerap tampak dalam bentuk “kelinglungan” dan kelumpuhan. Dengan kata lain, kucing yang terinfeksi rabies akan terus-menerus terlihat lesu, bingung, dan sakit.[4] Pada rabies jenis ini, kucing umumnya tidak ganas dan bahkan tidak akan berusaha menggigit. Gejala-gejalanya meliputi:[5]</p>
                    <li>kelumpuhan (ketidakmampuan menggerakkan) kaki, otot-otot wajah, atau bagian tubuh yang lain.</li>
                    <li>penurunan rahang bagian bawah, sehingga membuat wajah terlihat bodoh dan linglung</li>
                    <li>air liur berlebih yang membentuk busa di sekitar mulut</li>
                    <li>kesulitan menelan</li>
                    <center><img height="300px" width ="auto" src="asset/img/desainpelaporan/kucingrabies3.jpg" alt=""> </center>
                    <h3 class="mt-3">3. Waspadai gejala rabies yang berbahaya.</h3>
                    <p>Kucing yang terinfeksi virus rabies berbahaya kerap bertindak agresif, berperilaku ganjil, dan mulutnya berbusa. Meski tanda-tanda tersebut sangat identik dengan penyakit rabies, Anda tidak akan menemukannya pada rabies tipe “lumpuh”. Jika kucing Anda menunjukkan gejala rabies yang berbahaya, segera hubungi petugas kontrol hewan. Hati-hati, kucing yang terinfeksi rabies berbahaya rentan melakukan serangan, jadi jangan pernah mencoba menangkapnya sendirian. Tanda-tanda rabies berbahaya adalah:[6]</p>
                    <li>air liur yang melimpah sehingga terlihat seperti busa di sekitar mulut kucing</li>
                    <li>fobia air, yaitu takut berada di dekat air atau bahkan takut mendengar suara air</li>
                    <li>keagresifan, seperti selalu memperlihatkan gigi seakan-akan siap menggigit</li>
                    <li>kegelisahan</li>
                    <li>hilangnya ketertarikan terhadap makanan</li>
                    <li>suka menggigit atau menyerang</li>
                    <li>memperlihatkan perilaku yang ganjil, seperti menggigiti badannya sendiri</li>

                    <br>
                    <p>source: <a href="https://id.wikihow.com/Mengenali-Kucing-yang-Terinfeksi-Rabies"> wikihow </a></p>

                    <p class="mt-3">Sedangkan ciri-ciri anjing yang yang terjangkit rabies sebagai berikut :</p>
                    <center><img height="300px" width ="auto" src="asset/img/desainpelaporan/anjingrabies.jpg" alt=""></center>
                    <h3 class="mt-3">1. Cari tanda-tanda awal infeksi rabies.</h3>
                    <p class="mt-3"> Tahap awal ini bisa berlangsung selama 2-10 hari. Selama periode ini, anjing akan tampak sakit dengan menunjukkan gejala yang umum terjadi. Jika gejala-gejala ini muncul, cari bekas gigitan atau tanda bekas perkelahian (keropeng, goresan, bulu yang kusut serta air liur kering) pada anjing Anda. Jika Anda menemukan bekas gigitan atau luka apa pun, segera bawa anjing ke dokter hewan untuk diperiksa. Gejala-gejala awal yang umum ini termasuk:[3]</p>
                    <li>Nyeri otot</li>
                    <li>Gelisah</li>
                    <li>Cepat marah</li>
                    <li>Panas dingin</li>
                    <li>demam</li>
                    <li>Tidak enak badan, merasa sakit dan tidak nyaman</li>
                    <li>Photophobia, takut terhadap cahaya yang terang</li>
                    <li>Anoreksia, atau tidak nafsu makan</li>
                    <li>Muntah</li>
                    <li>Diare</li>
                    <li>Batuk</li>
                    <li>Tidak mampu atau tidak mau menelan</li>
                    <center><img class="mt-3" height="300px" width ="auto" src="asset/img/desainpelaporan/anjingrabies2.jpg" alt=""></center>
                    <h3 class="mt-3">2. Cari gejala lanjutan dari bentuk rabies yang jinak.</h3>
                    <p class="mt-3">Bentuk rabies yang lebih jinak ini, dikenal juga sebagai rabies bentuk paralisis atau dumb, adalah yang paling umum ditemui dan bisa berlangsung selama 3-7 hari. Bentuk rabies ini dikenal sebagai rabies paralisis atau dumb, karena anjing mungkin mengeluarkan busa di sekitar mulutnya atau menjadi lumpuh. Anjing juga akan tampak bingung, sakit, atau lesu (lelah).[4] Segera bawa anjing ke dokter hewan jika gejala lain bentuk rabies ini muncul, termasuk:[5]
                    </p>
                    <li>Paralisis (ketidakmampuan untuk bergerak) pada kaki, otot wajah, atau bagian tubuh lainnya. Gejala ini biasanya mulai muncul pada kaki bagian belakang dan bergerak ke tubuh bagian depan.</li>
                    <li>Turunnya rahang bagian bawah, membuat anjing terlihat 'bodoh'.</li>
                    <li>Membuat suara gonggongan yang aneh dan tidak terdengar seperti gonggongan biasanya.</li>
                    <li>Mengeluarkan liur secara berlebihan sehingga membentuk busa di sekitar mulutnya.</li>
                    <li>Kesulitan menelan.</li>
                    <li>Dalam bentuk rabies ini, anjing tidak ganas dan jarang mencoba menggigit.</li>
                    <center><img class="mt-3" height="300px" width ="auto" src="asset/img/desainpelaporan/anjingrabies3.jpg" alt=""></center>
                    <h3 class="mt-3">3. Cari gejala lanjutan dari bentuk rabies yang agresif.</h3>
                    <p class="mt-3">Bentuk ini juga berlangsung selama 3-7 hari dan anjing akan terlihat agresif atau perasaannya mudah meluap.[6] Anjing mungkin berperilaku aneh dan mulutnya berbusa. Bentuk ini adalah gambaran umum yang orang awam pikir tentang rabies. Namun, dibanding rabies bentuk paralisis atau dumb, bentuk ini lebih jarang ditemukan pada anjing. Bentuk rabies ini menghasilkan agresi berlebih yang harus ditangani dengan sangat hati-hati agar anjing tidak menggigit. Hubungi otoritas veteriner jika Anda berpikir ada anjing yang terkena jenis rabies ini. Gejala-gejalanya adalah:[7]</p>
                    <li>Mengeluarkan liur secara berlebihan sehingga mulutnya tampak berbusa.</li>
                    <li>Hydrophobia, takut terhadap air. Anjing tidak akan mendekati air dan akan tampak gelisah atau panik saat tersentuh atau ada suara air.</li>
                    <li>Agresif. Anjing mungkin tampak akan menggigit dan tampak kejam dengan menunjukkan giginya.</li>
                    <li>Gelisah atau merasa tidak nyaman. Anjing juga mungkin tidak nafsu makan.</li>
                    <li>Cepat marah. Provokasi sekecil apa pun bisa membuat anjing menyerang dan menggigit. Anjing juga mungkin melakukannya tanpa provokasi atau penyebab apa pun.</li>
                    <li>Perilaku yang tidak normal seperti mengunyah batu, sampah, atau kakinya sendiri. Anjing juga mungkin akan mengikuti dan mencoba menggigit tangan Anda jika Anda melambaikan tangan di depannya saat anjing berada di kandang.</li>
                    <li>Anak anjing yang sangat ceria tiba-tiba menggigit saat dielus dan menjadi ganas setelah beberapa jam.</li>
                    <center><img class="mt-3" height="300px" width ="auto" src="asset/img/desainpelaporan/anjingrabies4.jpg" alt=""> </center>
                    <h3 class="mt-3">4. Cari bekas gigitan atau luka terbuka pada anjing.</h3>
                    <p>Saat hewan yang terinfeksi menggigit hewan lainnya, rabies menular melalui liurnya. Penyakit ini menular saat liur hewan yang terinfeksi menyentuh darah dan membran mukosa (mulut, mata, dan rongga hidung) hewan lain. Pencarian bekas gigitan atau luka terbuka akan membantu Anda dalam menentukan apakah anjing terkena rabies atau tidak.</p>
                    <li>Saat masuk ke dalam tubuhnya, rabies menyebar melalui saraf hingga mencapai sistem saraf utama (saraf tulang belakang dan otak).[8] Dari sana, rabies menyebar melalui kelenjar ludah dan siap untuk menyebar ke korban lainnya.</li>
                   <center><img class="mt-3" height="300px" width ="auto" src="asset/img/desainpelaporan/anjingrabies5.jpg" alt=""></center>
                    <h3 class="mt-3">5. Segera hubungi petugas medis.</h3>
                    <p class="mt-3">Jika sudah digigit, bawa anjing Anda ke dokter hewan sesegera mungkin.[9] Virus rabies akan hidup di kulit atau bulu anjing selama dua jam. Jadi, pakailah sarung tangan, baju tangan panjang dan celana panjang sebelum menanganinya. Selain memeriksa anjing, dokter hewan juga akan menanyakan Anda tentang kemungkinan kontak terhadap rabies (seperti apakah Anda mencium bau sigung di halaman Anda atau apakah anjing terekspos rakun atau kelelawar di area tersebut).</p>
                    <li>Jika Anda melihat ada anjing (bukan milik Anda) yang mungkin terinfeksi, hubungi otoritas veteriner. Dengan begitu, anjing bisa dibawa ke dokter hewan tanpa berisiko membuat Anda terkena gigitannya.</li>
                    <li>Tidak ada tes yang bisa menentukan apakah hewan yang masih hidup terinfeksi rabies atau tidak. Satu-satunya tes yang ada adalah dengan mengangkat otak dari tubuh dan memeriksa bagian kecil otak secara mikroskopik untuk mencari tanda-tanda spesifik yang disebut badan negri.[10]</li>
                     <center><img class="mt-3" height="300px" width ="auto" src="asset/img/desainpelaporan/anjingrabies6.jpg" alt=""></center>
                     <h3 class="mt-3">6. Pahami tindakan medis apa yang bisa dilakukan untuk anjing.</h3>
                    <p class="mt-3">Jika sebelumnya pernah divaksin, anjing akan diberi vaksin rabies tambahan. Hal ini akan membantu sistem imunnya melawan virus. Anjing juga akan dipantau dengan ketat selama 45 hari. Pemantauan ini biasanya bisa dilakukan di rumah. Selama periode ini, anjing juga harus menjauhi kontak dengan hewan lain dan orang di luar rumah.[11] Jika anjing belum pernah divaksinasi dan digigit oleh hewan yang dikonfirmasi terinfeksi rabies, eutanasia biasanya akan direkomendasikan.</p>
                    <li>Melakukan eutanasia pada anjing mencegah risiko kesehatan serius terhadap manusia dan menjaga supaya anjing tidak benar-benar terinfeksi rabies.</li>
                    <li>Jika Anda menolak untuk mengeutanasianya, anjing akan dikarantina dan diobservasi selama 6 bulan di klinik hewan yang bersedia melakukannya. Biayanya akan ditanggung oleh Anda. Jika tidak terinfeksi, anjing akan divaksinasi sebulan sebelum dilepaskan.</li>
                     <center><img class="mt-3" height="300px" width ="auto" src="asset/img/desainpelaporan/anjingrabies7.jpg" alt=""></center>
                     <h3 class="mt-3">7. Pahami bahwa beberapa penyakit bisa tampak seperti rabies.</h3>
                    <p class="mt-3"> Jika tidak ada bekas gigitan apa pun pada anjing, namun Anda khawatir dengan gejala-gejala yang muncul, anjing mungkin terkena penyakit lain (yang bisa tampak seperti rabies). Segera bawa anjing Anda ke dokter hewan jika anjing tampak sakit atau menunjukkan gejala-gejala yang aneh. Beberapa di antara penyakit dan kondisi yang bisa disangka rabies adalah:[12]</p>
                    <li>Hepatitis anjing</li>
                    <li>Meningitis</li>
                    <li>Tetanus</li>
                    <li>Toxoplasmosis</li>
                    <li>Tumor otak</li>
                    <li>Agresi maternal pada anjing betina yang baru melahirkan</li>
                    <li>Keracunan zat kimia seperti diminazene atau organofosfat</li>

